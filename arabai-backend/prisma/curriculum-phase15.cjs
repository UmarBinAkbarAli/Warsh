  "جواب دیکھنے سے پہلے عربی کو آواز Ú©Û’ ساتھ پڑھیں۔ آہستہ اور درست دہرانا جلدی کرنے سے بہتر ÛÛ’Û”";
const DEFAULT_QUIZ_TIP_EN =
  "Eliminate one wrong option first, then compare the remaining choices carefully.";
const DEFAULT_QUIZ_TIP_UR =
  "پہلے ایک غلط جواب نکالیں، پھر باقی اختیارات کو غور سے دیکھیں۔";

function word(arabic, transliteration, meaningEn, meaningUr) {
  return {
    arabic,
    transliteration,
    meaning_en: meaningEn,
    meaning_ur: meaningUr,
  };
}

function flashcardLesson({
  title,
  titleAr,
  prompt,
  answer,
  focus,
  hook,
  explanationEn,
  explanationUr,
  quranicExample,
  conversationExample,
  reviewWords = [],
  tipEn = DEFAULT_FLASHCARD_TIP_EN,
  tipUr = DEFAULT_FLASHCARD_TIP_UR,
}) {
  return {
    title,
    titleAr,
    type: "FLASHCARD",
    xpReward: 10,
    content: {
      question: prompt ?? focus?.arabic ?? title,
      answer,
      arabic: focus?.arabic,
      transliteration: focus?.transliteration,
      meaning_en: focus?.meaning_en,
      meaning_ur: focus?.meaning_ur,
      hook,
      explanation_en: explanationEn,
      explanation_ur: explanationUr,
      quranic_example: quranicExample,
      conversation_example: conversationExample,
      ustadh_noor_tip_en: tipEn,
      ustadh_noor_tip_ur: tipUr,
      review_words: reviewWords.filter(Boolean),
      example: quranicExample ? `${quranicExample.arabic} — ${quranicExample.meaning_en}` : undefined,
    },
  };
}

function quizLesson({
  title,
  titleAr,
  type = "FILL_BLANK",
  question,
  questionUr,
  answer,
  options,
  hook,
  focus,
  explanationEn,
  explanationUr,
  quranicExample,
  conversationExample,
  reviewWords = [],
  tipEn = DEFAULT_QUIZ_TIP_EN,
  tipUr = DEFAULT_QUIZ_TIP_UR,
}) {
  return {
    title,
    titleAr,
    type,
    xpReward: 10,
    content: {
      question,
      question_ur: questionUr,
      answer,
      options,
      arabic: focus?.arabic,
      transliteration: focus?.transliteration,
      meaning_en: focus?.meaning_en,
      meaning_ur: focus?.meaning_ur,
      hook,
      explanation_en: explanationEn,
      explanation_ur: explanationUr,
      quranic_example: quranicExample,
      conversation_example: conversationExample,
      ustadh_noor_tip_en: tipEn,
      ustadh_noor_tip_ur: tipUr,
      review_words: reviewWords.filter(Boolean),
    },
  };
}

function chapter(order, title, titleAr, description, worldMapX, worldMapY, lessons) {
  return {
    order,
    title,
    titleAr,
    description,
    worldMapX,
    worldMapY,
    isLocked: order !== 1,
    lessons,
  };
}

